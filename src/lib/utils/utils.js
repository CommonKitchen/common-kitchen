/**
 * Форматирует чистый номер телефона в читаемый вид: +XX(XXX)XXX-XX-XX
 * @param {string} num - Чистый номер телефона (например, '+380990000100').
 * @returns {string} Форматированный номер.
 */
export function formatPhoneNumber(num) {
	// 1. Убираем все, кроме цифр и знака '+'
	const cleaned = num.replace(/[^+\d]/g, '');

	// 2. Проверяем, соответствует ли строка формату (например, +380990000100)
	// Группы: [+][38][099][000][01][00]
	const match = cleaned.match(/^(\+\d{2})(\d{3})(\d{3})(\d{2})(\d{2})$/);

	if (match) {
		// 3. Собираем форматированную строку из групп
		return `${match[1]}(${match[2]}) ${match[3]}-${match[4]}-${match[5]}`;
	}
	// Возвращаем исходный номер, если формат не подходит
	return num;
}
